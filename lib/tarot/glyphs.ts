/**
 * 타로 카드 표기 보조 — 로마자 번호 + 점성 글리프.
 * 카드 컴포넌트(앞면)와 사전 열람 페이지가 공유.
 */

const ROMAN = [
  "0", "I", "II", "III", "IV", "V", "VI", "VII", "VIII", "IX", "X",
  "XI", "XII", "XIII", "XIV", "XV", "XVI", "XVII", "XVIII", "XIX", "XX", "XXI",
] as const;

/** 메이저 번호(0~21) → 로마자. 범위 밖은 문자열 변환 폴백. */
export function toRoman(id: number): string {
  return ROMAN[id] ?? String(id);
}

